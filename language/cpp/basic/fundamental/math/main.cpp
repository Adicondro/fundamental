#include <cmath>
#include <iostream>

using namespace std;

int main(int argc, char *argv[]) {
  cout << min(5, 10) << endl; // Lowest value
  cout << max(4, 20) << endl; // Highest Value
  cout << sqrt(64) << endl; // Square
  cout << round(2.6) << endl; // Round number
  cout << log(2) << endl; // Log 2

  return 0;
}
