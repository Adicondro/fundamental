#include <iostream>

using namespace std;

int main() {

  // 1. Creating Variables

  int myNum;
  myNum = 15;
  cout << myNum << endl;

  int myNum2;
  myNum2 = 20;
  cout << myNum2 << endl;


  // 2. Changing Variable Values
  int myNum3 = 1;
  myNum3 = 10;
  cout << myNum3 << endl;

  // 3. Other Types
  int myNum4 = 25;
  double myFloatnum = 0.25;
  char myChar = 'a';
  string myString = "Test";
  bool myBool = true;

  cout << myNum4 << "\n" << myFloatnum << "\n" << myChar << endl << myString << endl << myBool << endl;

  return 0;
}
