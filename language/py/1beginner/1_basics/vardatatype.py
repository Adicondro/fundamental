# Variables and Data Types in Python

# Integer
my_int = 10
print("Integer:", my_int)

# Float
my_float = 10.5
print("Float:", my_float)

# String
my_string = "Hello, World!"
print("String:", my_string)

# Boolean
my_bool = True
print("Boolean:", my_bool)

# List
my_list = [1, 2, 3, 4, 5]
print("List:", my_list)

# Tuple
my_tuple = (1, 2, 3, 4, 5)
print("Tuple:", my_tuple)

# Dictionary
my_dict = {"name": "Alice", "age": 25}
print("Dictionary:", my_dict)

# Set
my_set = {1, 2, 3, 4, 5}
print("Set:", my_set)


# Changing Data Types

# Integer to Float
int_to_float = float(my_int)
print("Integer to Float:", int_to_float)

# Float to Integer
float_to_int = int(my_float)
print("Float to Integer:", float_to_int)

# String to Integer
str_to_int = int("123")
print("String to Integer:", str_to_int)

# Integer to String
int_to_str = str(my_int)
print("Integer to String:", int_to_str)

# List to Tuple
list_to_tuple = tuple(my_list)
print("List to Tuple:", list_to_tuple)

# Tuple to List
tuple_to_list = list(my_tuple)
print("Tuple to List:", tuple_to_list)

# List to Set
list_to_set = set(my_list)
print("List to Set:", list_to_set)

# Set to List
set_to_list = list(my_set)
print("Set to List:", set_to_list)

# Dictionary Keys to List
dict_keys_to_list = list(my_dict.keys())
print("Dictionary Keys to List:", dict_keys_to_list)

# Dictionary Values to List
dict_values_to_list = list(my_dict.values())
print("Dictionary Values to List:", dict_values_to_list)